# VARIABLES

# LAS VARIABLES EN PYTHON SE DEFINEN CON (snake_case) ejemplo 

nombre_del_curso = 'Python dictado por Dalto'
print(nombre_del_curso)

a = 15
b = 8
c = a + b

print(c)

nombre = "Daniel"
print(nombre)

numero = 10
numero += 5
numero += 50

print(numero)

# CONCATENACION con + 

nombre = 'Daniel'
bienvenida = 'Hola ' + nombre + ' ¿Como estas?'
print(bienvenida)


# CONCATENACION CON (f) RAMBIEN SON LLAMADAS (f-strings)

nombre = 'Daniel'
bienvenida = f'Hola {nombre} ¿Como estas?'
print(bienvenida)


# OPERADORES DE PERTENENCIA (in) - (not in)

nombre = 'Daniel'
bienvenida = f'Hola {nombre} ¿Como estas?'
print('Hola' not in bienvenida)
print('Hola' in bienvenida)

