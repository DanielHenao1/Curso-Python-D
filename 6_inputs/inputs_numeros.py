
# TENER EN CUENTA QUE LOS DATOS POR DEFECTOS QUE SE NOS DEVUELVE SON CADENAS DE TEXTO

# Pedirle un numero al usuario

numero = input('indicame un numero: ')

# Tenemos que pasar el dato a numero con la funcion (init) 

# Multiplicando el numero * 2

resultado_entero = int(numero) * 2

print(resultado_entero)

# Tambien lo podemos pasar a numero flotante con la funcion (float)

resultado_flotante = float(numero) * 2

# Imprimiento el resultado

print(resultado_flotante)